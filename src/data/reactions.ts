export const findReaction = (reactantSymbols: string[]): Reaction | null => {
  // Sort reactants for comparison
  const sortedReactants = [...reactantSymbols].sort();
  
  for (const reaction of reactions) {
    const reactionReactants = [...reaction.reactants].sort();
    
    // Check if reactants match (allowing for different quantities)
    const matches = sortedReactants.every(symbol => 
      reactionReactants.includes(symbol)
    ) && reactionReactants.every(symbol => 
      sortedReactants.includes(symbol)
    );
    
    if (matches) {
      return reaction;
    }
  }
  
  return null;
};

export const getReactionsByType = (type: string): Reaction[] => {
  return reactions.filter(reaction => reaction.type === type);
};
